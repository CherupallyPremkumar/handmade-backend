package com.handmade.ecommerce.paymentauthorization.configuration.controller;

import java.util.Map;

import jakarta.servlet.http.HttpServletRequest;

import org.chenile.base.response.GenericResponse;
import org.chenile.http.annotation.BodyTypeSelector;
import org.chenile.http.annotation.ChenileController;
import org.chenile.http.annotation.ChenileParamType;
import org.chenile.http.handler.ControllerSupport;
import org.springframework.http.ResponseEntity;

import org.chenile.stm.StateEntity;

import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PatchMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;
import org.chenile.workflow.dto.StateEntityServiceResponse;
import com.handmade.ecommerce.payment.model.PaymentAuthorization;

@RestController
@ChenileController(value = "paymentauthorizationService", serviceName = "_paymentauthorizationStateEntityService_",
		healthCheckerName = "paymentauthorizationHealthChecker")
public class PaymentAuthorizationController extends ControllerSupport{
	
	@GetMapping("/paymentauthorization/{id}")
	public ResponseEntity<GenericResponse<StateEntityServiceResponse<PaymentAuthorization>>> retrieve(
			HttpServletRequest httpServletRequest,
			@PathVariable String id){
		return process(httpServletRequest,id);
	}

	@PostMapping("/paymentauthorization")
	public ResponseEntity<GenericResponse<StateEntityServiceResponse<PaymentAuthorization>>> create(
			HttpServletRequest httpServletRequest,
			@ChenileParamType(StateEntity.class)
			@RequestBody PaymentAuthorization entity){
		return process(httpServletRequest,entity);
	}

	
	@PatchMapping("/paymentauthorization/{id}/{eventID}")
	@BodyTypeSelector("paymentauthorizationBodyTypeSelector")
	public ResponseEntity<GenericResponse<StateEntityServiceResponse<PaymentAuthorization>>> processById(
			HttpServletRequest httpServletRequest,
			@PathVariable String id,
			@PathVariable String eventID,
			@ChenileParamType(Object.class) 
			@RequestBody String eventPayload){
		return process(httpServletRequest,id,eventID,eventPayload);
	}


}
