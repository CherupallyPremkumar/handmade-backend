package com.handmade.ecommerce.sellerkyc.configuration.controller;

import java.util.Map;

import jakarta.servlet.http.HttpServletRequest;

import org.chenile.base.response.GenericResponse;
import org.chenile.http.annotation.BodyTypeSelector;
import org.chenile.http.annotation.ChenileController;
import org.chenile.http.annotation.ChenileParamType;
import org.chenile.http.handler.ControllerSupport;
import org.springframework.http.ResponseEntity;

import org.chenile.stm.StateEntity;

import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PatchMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;
import org.chenile.workflow.dto.StateEntityServiceResponse;
import com.handmade.ecommerce.seller.model.SellerKyc;

@RestController
@ChenileController(value = "sellerkycService", serviceName = "_sellerkycStateEntityService_",
		healthCheckerName = "sellerkycHealthChecker")
public class SellerKycController extends ControllerSupport{
	
	@GetMapping("/sellerkyc/{id}")
	public ResponseEntity<GenericResponse<StateEntityServiceResponse<SellerKyc>>> retrieve(
			HttpServletRequest httpServletRequest,
			@PathVariable String id){
		return process(httpServletRequest,id);
	}

	@PostMapping("/sellerkyc")
	public ResponseEntity<GenericResponse<StateEntityServiceResponse<SellerKyc>>> create(
			HttpServletRequest httpServletRequest,
			@ChenileParamType(StateEntity.class)
			@RequestBody SellerKyc entity){
		return process(httpServletRequest,entity);
	}

	
	@PatchMapping("/sellerkyc/{id}/{eventID}")
	@BodyTypeSelector("sellerkycBodyTypeSelector")
	public ResponseEntity<GenericResponse<StateEntityServiceResponse<SellerKyc>>> processById(
			HttpServletRequest httpServletRequest,
			@PathVariable String id,
			@PathVariable String eventID,
			@ChenileParamType(Object.class) 
			@RequestBody String eventPayload){
		return process(httpServletRequest,id,eventID,eventPayload);
	}


}
